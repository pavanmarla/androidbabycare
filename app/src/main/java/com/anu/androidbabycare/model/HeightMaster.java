public class HeightMaster {

    private Integer id;
    private Integer age;
    private Integer valueLow;
    private Integer valueHigh;

    public Integer getId() {
        return id;
    }

    public void setId(Integer id) {
        this.id = id;
    }

    public Integer getAge() {
        return age;
    }

    public void setAge(Integer age) {
        this.age = age;
    }

    public Integer getValueLow() {
        return valueLow;
    }

    public void setValueLow(Integer valueLow) {
        this.valueLow = valueLow;
    }

    public Integer getValueHigh() {
        return valueHigh;
    }

    public void setValueHigh(Integer valueHigh) {
        this.valueHigh = valueHigh;
    }

}
